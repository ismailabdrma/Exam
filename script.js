function afficherVosInformations() {

  var nom = prompt("Veuillez saisir votre nom :");

  var prenom = prompt("Veuillez saisir votre prénom :");

  var filiere = prompt("Veuillez saisir votre filière :");

  var groupe = prompt("Veuillez saisir votre groupe :");

  alert("Nom : " + nom + "\nPrénom : " + prenom + "\nFilière : " + filiere + "\nGroupe : " + groupe);

}

